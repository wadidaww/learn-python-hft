# Entry point
# ---------------------------------------------------------------------------

if __name__ == "__main__":
    import random

    book = LimitOrderBook("AAPL")
    n_orders = 10_000
    oid = 0

    t0 = time.perf_counter_ns()
    for _ in range(n_orders // 2):
        # Add bids around 100.00
        book.add_order(Order(oid, "AAPL", Side.BID, round(99.5 + random.random(), 2), 100))
        oid += 1
        # Add asks around 100.50
        book.add_order(Order(oid, "AAPL", Side.ASK, round(100.5 + random.random(), 2), 100))
        oid += 1

    elapsed = (time.perf_counter_ns() - t0) / 1e6
    print(f"Inserted {n_orders} orders in {elapsed:.2f} ms ({elapsed*1e6/n_orders:.0f} ns/order)")
    print(f"Best bid: {book.best_bid()}  Best ask: {book.best_ask()}  Spread: {book.spread()}")
    print(f"Top 3 depth: {book.depth(3)}")
